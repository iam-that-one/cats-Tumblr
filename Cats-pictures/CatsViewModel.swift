class CatsViewModel: ObservableObject{
    @Published var cats = [Cat]()
    func getCat(){
        
        let headers = ["x-api-key": "DEMO-API-KEY"]
        
        let request = NSMutableURLRequest(url: NSURL(string: "https://api.thecatapi.com/v1/images/search")! as URL,
                                          cachePolicy: .useProtocolCachePolicy,
                                          timeoutInterval: 10.0)
        request.httpMethod = "GET"
        request.allHTTPHeaderFields = headers
        
        let session = URLSession.shared
        let dataTask = session.dataTask(with: request as URLRequest, completionHandler: { (data, response, error) -> Void in
            if (error != nil) {
                print(error!)
            } else {
                if let data = data{
                    do{
                        let result = try JSONDecoder().decode([Cat].self, from: data)
                        print(result)
                        DispatchQueue.main.async {
                            self.cats.append(contentsOf: result)
                        }
                    }catch{
                        print(error)
                    }
                }
              //  let httpResponse = response as? HTTPURLResponse
              //  print(httpResponse)
            }
        })
        
        dataTask.resume()
    }
    func loadImage(ImageUrl: String) -> Data?{
        guard let url = URL(string: ImageUrl) else {return Data()}
        if let data = try? Data(contentsOf: url){
            return data
        }
        return nil
    }
}

